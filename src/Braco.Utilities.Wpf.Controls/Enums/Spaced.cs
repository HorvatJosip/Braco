namespace Braco.Utilities.Wpf.Controls
{
	/// <summary>
	/// Defines how can grid's rows or columns be spaced.
	/// </summary>
	public enum Spaced
	{
		/// <summary>
		/// No spacing will be added.
		/// </summary>
		None,

		/// <summary>
		/// Spacing will be added between the rows / columns.
		/// </summary>
		Between,

		/// <summary>
		/// Spacing will be added and the rows / columns.
		/// </summary>
		Around,

		/// <summary>
		/// Spacing will be added  both between and around rows / columns.
		/// </summary>
		AroundAndBetween
	}
}
